#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
