#include "fillit.h"

int	map_min_size(char **tab)
{
	int	i;
	int	j;

	i = 0;
	j = 2;
	while (ft_strcmp(tab[i], "end") != 0)
		i++;
	i = i * 4;
	while (j * j < i)
		j++;
	return (j);
}
